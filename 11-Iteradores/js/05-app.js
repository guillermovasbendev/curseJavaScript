// EL DO WHILE
// Se ejecuta al menos una vez y luego verifica la condicion
let i = 10000; //inicio

do {
  console.log(`${i}`); //dentro del do va lo Que se ejecuta almenos una vez
  i++; //el incremento va antes de finalizar el do
} while (i < 10); //la condicion esta dentro del () del while
{
}
// el do while, al menos se ejecuta una vez y luego revisa la condicion
