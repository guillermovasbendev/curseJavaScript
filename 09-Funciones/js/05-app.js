// parametros y argumentos
function sumar(a, b) {
  //son parametros de funcion
  console.log(a + b);
}
// para llamarlo debo dar valor a esos parametros

sumar(3, 7); //3, y 7 son argumentos valores reales que reemplazan los parametros

function saludar(nombre, apellido) {
  //invento parametro nombre y parametro apellido
  console.log(`Hola ${nombre} ${apellido}`);
}

saludar("Guillermo", "Vasquez"); //doy argumentos reales en la ejecucion
