// los arreglos agrupan elementos del mismo tipo, pueden tener multiples elementos que son iguales
// se definen con [ ] corchetes
const numeros = [10, 20, 30];
// arreglos solo tiene valor a diferencia de objetos que tienen llave y valor

// crear arreglos con new  Aqui no se usa en la creacion [ ] sino ()
const meses = new Array(`enero`, `febrero`, `marzo`);

console.log(numeros);
console.log(meses);

// el arreglo puede tener varios tipos de datos
const varios = ["hola", 37, true, null, { nombre: "guillermo" }];
console.log(varios);
